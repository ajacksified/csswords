function translateName(color) {
  var translated = color
                    .replace(/O/g, '0')
                    .replace(/L/g, '1')
                    .replace(/Z/g, '2')
                    .replace(/S/g, '5')
                    .replace(/G/g, '6')
                    .replace(/T/g, '7');
  return '#' + translated;
}

function loadFile() {
  var colorList = document.getElementById('colorList');
  var colorListFragment = document.createDocumentFragment();
  var element, color;

  colors.forEach(function(name){
    color = translateName(name);
    element = document.createElement('li');

    element.innerHTML = 
      '<span class="name">' + name + '</span>' + 
      '<span class="hex">' + color + '</span>';

    element.style.backgroundColor = color;

    colorListFragment.appendChild(element);
  });

  colorList.appendChild(colorListFragment);
}

window.onload = loadFile;
